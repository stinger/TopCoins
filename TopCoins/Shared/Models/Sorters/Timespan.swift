import Foundation

enum Timespan: String, CaseIterable {
    case oneHour = "1h"
    case twelveHours = "12h"
    case twentyFourHours = "24h"
    case oneWeek = "7d"
    case thirtyDays = "30d"
    case oneYear = "1y"
}
